# while式　繰り返し処理
#指定した条件によって繰り返しの有無を判断

dice = 0    # 変数diceに0を代入し、初期値を設定

#6が出るまで処理が行われる
while dice != 6 do  # サイコロの目が6ではない間、diceの初期値は0なので条件を満たす。以降はdiceに代入される数によって結果が異なる
  dice = rand(1..6)   #１～６の整数がランダムに出力される
  puts dice
end


# rand：乱数　ランダムに数値を出力

#処理が終わらないコードを記述してしまうと無限ループになる
#この場合は強制的に処理を終わらす
