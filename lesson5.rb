#if式を用いる
#所持金が100円より大きい場合は、"みかんを購入。所持金に余りあり。"と出力
#所持金が100円ちょうどの場合は、"みかんを購入。所持金は0円。"と出力
#所持金が100円より少ない場合は、"みかんを購入することができません。"と出力
#変数名はtotal_money

total_money = 60

if total_money > 100
  puts "みかんを購入。所持金に余りあり。"
elsif total_money == 100
  puts "みかんを購入。所持金に余りあり。"
else
  puts "みかんを購入することができません。"
end
